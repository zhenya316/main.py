name = 'Zhenya'     # переменная ссылается на обьект
print(name, type(name))     # Динамический типизация, тип данный может меняться
name = 1
print(name, type(name))
name = 1.1
print(name, type(name))
name = False
print(name, type(name))
name = [1, 3]
print(name, type(name))     # вначале создаётся обьект справа и связываеться с переменной, если уже есть то перезаписывает


